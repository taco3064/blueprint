const BROWNFIELD_CONFIG = `// Authored during the e2e as the known-good translation of the fixture.
export default {
  name: 'brownfield-fixture',
  framework: 'react',
  architecture: {
    alias: '@',
    layers: [
      { name: 'pages', does: 'routes' },
      { name: 'features', does: 'feature slices' },
      { name: 'services', does: 'network', owns: ['axios'] },
      { name: 'utils', does: 'pure helpers' },
    ],
    module: { layout: 'flat', entry: 'index', private: [] },
    layerFiles: 'src/{layer}/**/*.{ts,tsx}',
  },
  rules: { cycles: 'error' },
  emit: { agents: ['claude'] },
};
`;

describe('e2e · brownfield (the full adoption arc)', () => {
  it('walks authoring → findings → baseline → references → wired/integrated quiet', async () => {
    useFixture('brownfield');

    // 1. No config: authoring flow, hand-written files untouched.
    await runInit(root, { install: false, log: silent });

    expect(exists('blueprint-authoring.md')).toBe(true);
    expect(read('CLAUDE.md')).toContain('Do not clobber');
    expect(read('eslint.config.js')).toContain('hand-maintained');

    // 2. The authored config: inspect finds exactly the planted debt.
    fs.writeFileSync(path.join(root, 'blueprint.config.mjs'), BROWNFIELD_CONFIG);

    const { findings, ok } = await runInspect(root, { log: silent });

    const byRule = findings.reduce<Record<string, number>>((acc, finding) => {
      acc[finding.rule] = (acc[finding.rule] ?? 0) + 1;

      return acc;
    }, {});

    expect(ok).toBe(false);
    expect(byRule['flow-violation']).toBe(3); // same-layer + two upward reaches
    expect(byRule.cycle).toBe(1); // services → utils → services

    // 3. Baseline locks the debt; the ratchet goes green.
    await runInspect(root, { updateBaseline: true, log: silent });

    const ratchet = await runInspect(root, { baseline: true, log: silent });

    expect(ratchet.ok).toBe(true);

    // 4. init with the config: references land next to hand-written files.
    await runInit(root, { install: false, log: silent });

    expect(exists('CLAUDE.blueprint.md')).toBe(true);
    expect(exists('eslint.config.blueprint.mjs')).toBe(true);
    expect(read('CLAUDE.md')).toContain('Do not clobber'); // still untouched

    // 5. Simulate the agent finishing the integration; re-init goes quiet.
    fs.appendFileSync(
      path.join(root, 'CLAUDE.md'),
      '\n## Architecture contract\n\nSee node_modules/@kekkai/blueprint/agent-contract.md\n',
    );

    fs.writeFileSync(
      path.join(root, 'eslint.config.js'),
      'import { emitLint } from \'@kekkai/blueprint\';\nimport blueprint from \'./blueprint.config.mjs\';\nexport default [...emitLint(blueprint)];\n',
    );

    fs.rmSync(path.join(root, 'CLAUDE.blueprint.md'));
    fs.rmSync(path.join(root, 'eslint.config.blueprint.mjs'));

    const quiet = await runInit(root, { install: false, log: silent });

    expect(exists('CLAUDE.blueprint.md')).toBe(false);
    expect(exists('eslint.config.blueprint.mjs')).toBe(false);

    const notes = quiet
      .filter((action): action is Extract<typeof action, { kind: 'instruct' }> =>
        action.kind === 'instruct')
      .map((action) => action.note)
      .join('\n');

    expect(notes).toContain('already integrates');
    expect(notes).toContain('already wires');
  });
});

// ─── Tier 1: cross-cutting behaviors the per-terrain happy paths don't cover ───

describe('e2e · ratchet catches a new violation (Tier 1)', () => {
  it('reddens only on debt introduced after the baseline is locked', async () => {
    useFixture('brownfield');
    fs.writeFileSync(path.join(root, 'blueprint.config.mjs'), BROWNFIELD_CONFIG);

    // Lock today's debt; the ratchet is green.
    await runInspect(root, { updateBaseline: true, log: silent });
    expect((await runInspect(root, { baseline: true, log: silent })).ok).toBe(true);

    // Introduce a NEW upward reach: utils → pages (not in the baseline).
    fs.writeFileSync(
      path.join(root, 'src/utils/noop.ts'),
      'import { Home } from \'@/pages/Home\';\nexport const noop = () => Home;\n',
    );

    const after = await runInspect(root, { baseline: true, log: silent });

    expect(after.ok).toBe(false);
    // Only the fresh finding surfaces — the baselined debt stays suppressed.
    expect(after.findings.every((f) => f.path.includes('utils/noop'))).toBe(true);
  });
});

