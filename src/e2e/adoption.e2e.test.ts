import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { afterEach, beforeEach, describe, expect, it } from 'vitest';

import { runInit } from '../bootstrap';
import { runInspect } from '../inspect';
import { reactPreset } from '../presets';

/**
 * Adoption end-to-end: real template shapes (committed under
 * fixtures/adoption/) copied to a tmpdir, then driven through the same
 * runInit / runInspect calls the CLI makes. This suite runs in the default
 * vitest set, so the husky pre-push hook and the release workflow both gate
 * on it before anything reaches npm.
 */

const FIXTURES = fileURLToPath(new URL('../../fixtures/adoption', import.meta.url));

let root: string;

const silent = () => {};

beforeEach(() => {
  root = fs.mkdtempSync(path.join(os.tmpdir(), 'bp-e2e-'));
});

afterEach(() => {
  fs.rmSync(root, { recursive: true, force: true });
});

function useFixture(name: string): void {
  fs.cpSync(path.join(FIXTURES, name), root, { recursive: true });
}

const read = (file: string) => fs.readFileSync(path.join(root, file), 'utf-8');
const exists = (file: string) => fs.existsSync(path.join(root, file));

describe('e2e · vite-react-ts (greenfield preset + alias surgery)', () => {
  it('wires the alias into vite.config and the commented tsconfig, and stays idempotent', async () => {
    useFixture('vite-react-ts');

    await runInit(root, { install: false, log: silent });

    // Vite surgery: resolve.alias + the node:url import, template shape intact.
    const vite = read('vite.config.ts');

    expect(vite).toContain('\'~app\': fileURLToPath(new URL(\'./src\', import.meta.url))');
    expect(vite).toContain('import { fileURLToPath, URL } from \'node:url\'');
    expect(vite).toContain('plugins: [react()]');

    // JSONC tsconfig surgery: paths inserted, comment preserved.
    const tsconfig = read('tsconfig.app.json');

    expect(tsconfig).toContain('"paths": { "~app/*": ["./src/*"] }');
    expect(tsconfig).toContain('/* Bundler mode */');

    expect(read('eslint.config.mjs')).toContain('Generated by @kekkai/blueprint init');

    // The tmpdir has no node_modules — inject the preset the config resolves to.
    const { ok } = await runInspect(root, { log: silent, loadConfig: async () => reactPreset() });

    expect(ok).toBe(true);

    // Second run: config exists → no greenfield surgery, files unchanged.
    await runInit(root, {
      install: false,
      log: silent,
      loadConfig: async () => reactPreset(),
    });

    expect(read('vite.config.ts')).toBe(vite);
    expect(read('tsconfig.app.json')).toBe(tsconfig);
  });
});

